	private static bool ShouldSuppressBoundaryException(MethodBase originalMethod, Exception exception)
	{
		if (originalMethod == null || exception == null)
		{
			return false;
		}
		if (exception is OutOfMemoryException || exception is AccessViolationException || exception is BadImageFormatException || exception is CannotUnloadAppDomainException)
		{
			return false;
		}
		MethodInfo methodInfo = originalMethod as MethodInfo;
		if (methodInfo == null || methodInfo.ReturnType != typeof(void))
		{
			return false;
		}
		string text = originalMethod.DeclaringType?.FullName ?? "";
		return text.StartsWith("ManagedCallbacks.", StringComparison.Ordinal) && text.EndsWith("CallbacksGenerated", StringComparison.Ordinal);
	}

	private static bool ShouldSuppressLifecycleException(MethodBase originalMethod, Exception exception)
	{
