	private static bool IsLifecycleBoundaryMethod(MethodInfo method)
	{
		if (method == null || method.IsAbstract || method.ContainsGenericParameters)
		{
			return false;
		}
		if (!LifecycleBoundaryMethodNames.Contains(method.Name))
		{
			return false;
		}
		if (method.GetMethodBody() == null)
		{
			return false;
		}
		ParameterInfo[] parameters;
		try
		{
			parameters = method.GetParameters();
		}
		catch
		{
			return false;
		}
		return parameters == null || parameters.Length <= 4;
	}

	private static bool IsCampaignDispatcherBoundaryMethod(MethodInfo method)
	{
		if (method == null || method.IsAbstract || method.ContainsGenericParameters || method.IsConstructor || method.IsSpecialName)
		{
			return false;
		}
