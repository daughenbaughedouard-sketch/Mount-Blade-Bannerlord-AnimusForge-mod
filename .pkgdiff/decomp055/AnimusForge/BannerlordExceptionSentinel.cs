		bool flag = forceDetailed;
		lock (ExceptionLogLock)
		{
			if (!ExceptionLogStates.TryGetValue(key, out var value))
			{
				value = new ExceptionLogState();
				ExceptionLogStates[key] = value;
			}
			value.Count++;
			num = value.Count;
			DateTime utcNow = DateTime.UtcNow;
			if (forceDetailed || value.Count == 1 || value.Count == 2 || value.Count == 5 || value.Count % 10 == 0 || utcNow - value.LastLoggedUtc > TimeSpan.FromSeconds(30.0))
			{
				flag = true;
				value.LastLoggedUtc = utcNow;
			}
		}
		if (flag)
		{
			string text5 = text + "." + text2;
			string text6 = "";
			if (!string.IsNullOrWhiteSpace(extra))
			{
				text6 = " extra=" + extra;
			}
			string text7 = exception?.ToString() ?? "(exception unavailable)";
			Logger.Log("ExceptionSentinel", $"[{category}] count={num} suppress={suppress} method={text5} exception={text3}: {text4}{text6}\n{text7}");
		}
	}
