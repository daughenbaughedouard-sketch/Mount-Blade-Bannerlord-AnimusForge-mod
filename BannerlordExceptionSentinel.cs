		{
			return false;
		}
		MethodInfo methodInfo = originalMethod as MethodInfo;
		if (methodInfo == null || methodInfo.ReturnType != typeof(void))
		{
			return false;
		}
		return IsLifecycleBoundaryMethod(methodInfo) && IsLifecycleBoundaryType(methodInfo.DeclaringType);
	}

	private static bool ShouldSuppressCampaignDispatcherException(MethodBase originalMethod, Exception exception)
