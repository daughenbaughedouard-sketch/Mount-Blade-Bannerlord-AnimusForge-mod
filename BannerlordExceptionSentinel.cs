	private static string BuildMethodKey(MethodBase method)
	{
		if (method == null)
		{
			return "null";
		}
		string text = "";
		try
		{
			ParameterInfo[] parameters = method.GetParameters();
			if (parameters != null && parameters.Length != 0)
			{
				text = string.Join(",", parameters.Select((ParameterInfo p) => p?.ParameterType?.FullName ?? "null"));
			}
		}
		catch
		{
			text = "?";
		}
		string text2 = "void";
		try
		{
			if (method is MethodInfo methodInfo)
			{
				text2 = methodInfo.ReturnType?.FullName ?? "void";
			}
		}
		catch
		{
			text2 = "unknown";
		}
		return $"{method.Module?.Name ?? "unknown"}::{method.DeclaringType?.FullName ?? "unknown"}.{method.Name}({text})->{text2}";
	}

