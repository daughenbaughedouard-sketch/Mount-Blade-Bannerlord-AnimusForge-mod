using System;
using System.Collections.Generic;
using System.Linq;
using System.Reflection;
using System.Threading;
using System.Threading.Tasks;
using System.Windows.Forms;
using HarmonyLib;

namespace AnimusForge;

internal static class BannerlordExceptionSentinel
{
	private sealed class ExceptionLogState
	{
		public int Count;

		public DateTime LastLoggedUtc;
	}

	private static readonly object InitLock = new object();

	private static readonly object ExceptionLogLock = new object();

	private static readonly HashSet<string> PatchedMethodKeys = new HashSet<string>(StringComparer.Ordinal);

	private static readonly Dictionary<string, ExceptionLogState> ExceptionLogStates = new Dictionary<string, ExceptionLogState>(StringComparer.Ordinal);

	private static readonly string[] TargetAutoGeneratedAssemblies = new string[3] { "TaleWorlds.DotNet.AutoGenerated", "TaleWorlds.Engine.AutoGenerated", "TaleWorlds.MountAndBlade.AutoGenerated" };

	private static readonly HashSet<string> LoadedTargetAutoGeneratedAssemblies = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

	private static Harmony _harmony;

	private static bool _initialized;

	private static bool _globalHandlersRegistered;

	private static bool _autoGeneratedPatchDiscoveryComplete;

	private static int _patchedAutoGeneratedMethodCount;

	private static int _patchDiscoveryAttempts;

	private static DateTime _nextPatchDiscoveryRetryUtc = DateTime.MinValue;

	internal static void Initialize(Harmony harmony)
	{
		if (harmony == null)
		{
			return;
		}
		lock (InitLock)
		{
			if (_initialized)
			{
				return;
			}
			_initialized = true;
			_harmony = harmony;
			RegisterGlobalHandlers();
			TryInstallPendingBoundaryPatches(forceSummaryLog: true);
		}
	}

	internal static void OnApplicationTick()
	{
		if (!_initialized || _autoGeneratedPatchDiscoveryComplete || _harmony == null)
		{
			return;
		}
		DateTime utcNow = DateTime.UtcNow;
		if (utcNow < _nextPatchDiscoveryRetryUtc)
		{
			return;
		}
		_nextPatchDiscoveryRetryUtc = utcNow.AddSeconds(3.0);
		TryInstallPendingBoundaryPatches(forceSummaryLog: false);
	}

	private static void RegisterGlobalHandlers()
	{
		if (_globalHandlersRegistered)
		{
			return;
		}
		try
		{
			AppDomain.CurrentDomain.UnhandledException += CurrentDomain_UnhandledException;
		}
		catch (Exception ex)
		{
			Logger.Log("ExceptionSentinel", "Failed to register AppDomain.UnhandledException: " + ex.Message);
		}
		try
		{
			TaskScheduler.UnobservedTaskException += TaskScheduler_UnobservedTaskException;
		}
		catch (Exception ex2)
		{
			Logger.Log("ExceptionSentinel", "Failed to register TaskScheduler.UnobservedTaskException: " + ex2.Message);
		}
		try
		{
			Application.ThreadException += Application_ThreadException;
		}
		catch (Exception ex3)
		{
			Logger.Log("ExceptionSentinel", "Failed to register Application.ThreadException: " + ex3.Message);
		}
		_globalHandlersRegistered = true;
		Logger.Log("ExceptionSentinel", "Global exception handlers registered.");
	}

	private static void CurrentDomain_UnhandledException(object sender, UnhandledExceptionEventArgs e)
	{
		Exception ex = e.ExceptionObject as Exception;
		LogException("UnhandledException", null, ex, suppress: false, $"terminating={e.IsTerminating};sender={sender?.GetType().FullName ?? "null"}", forceDetailed: true);
	}

	private static void TaskScheduler_UnobservedTaskException(object sender, UnobservedTaskExceptionEventArgs e)
	{
		LogException("UnobservedTaskException", null, e.Exception, suppress: true, $"sender={sender?.GetType().FullName ?? "null"}", forceDetailed: true);
		try
		{
			e.SetObserved();
		}
		catch
		{
		}
	}

	private static void Application_ThreadException(object sender, ThreadExceptionEventArgs e)
	{
		LogException("WinFormsThreadException", null, e.Exception, suppress: false, $"sender={sender?.GetType().FullName ?? "null"}", forceDetailed: true);
	}

	private static void TryInstallPendingBoundaryPatches(bool forceSummaryLog)
	{
		if (_harmony == null)
		{
			return;
		}
		int num = _patchedAutoGeneratedMethodCount;
		List<string> list = new List<string>();
		for (int i = 0; i < TargetAutoGeneratedAssemblies.Length; i++)
		{
			string text = TargetAutoGeneratedAssemblies[i];
			Assembly assembly = FindLoadedOrLoadAssembly(text);
			if (assembly == null)
			{
				continue;
			}
			list.Add(text);
			LoadedTargetAutoGeneratedAssemblies.Add(text);
			PatchAutoGeneratedBoundaryAssembly(assembly);
		}
		_patchDiscoveryAttempts++;
		if (LoadedTargetAutoGeneratedAssemblies.Count >= TargetAutoGeneratedAssemblies.Length)
		{
			_autoGeneratedPatchDiscoveryComplete = true;
		}
		if (forceSummaryLog || _patchedAutoGeneratedMethodCount != num || _autoGeneratedPatchDiscoveryComplete)
		{
			Logger.Log("ExceptionSentinel", $"Boundary patch scan finished. patched={_patchedAutoGeneratedMethodCount}, loadedAssemblies={string.Join(",", list)}, complete={_autoGeneratedPatchDiscoveryComplete}, attempts={_patchDiscoveryAttempts}");
		}
	}

	private static Assembly FindLoadedOrLoadAssembly(string simpleName)
	{
		if (string.IsNullOrWhiteSpace(simpleName))
		{
			return null;
		}
		try
		{
			Assembly assembly = AppDomain.CurrentDomain.GetAssemblies().FirstOrDefault((Assembly a) => a != null && string.Equals(a.GetName().Name, simpleName, StringComparison.OrdinalIgnoreCase));
			if (assembly != null)
			{
				return assembly;
			}
		}
		catch
		{
		}
		try
		{
			return Assembly.Load(simpleName);
		}
		catch
		{
			return null;
		}
	}

	private static void PatchAutoGeneratedBoundaryAssembly(Assembly assembly)
	{
		if (assembly == null)
		{
			return;
		}
		foreach (Type safeType in GetSafeTypes(assembly))
		{
			if (!IsManagedCallbackBoundaryType(safeType))
			{
				continue;
			}
			MethodInfo[] methods = null;
			try
			{
				methods = safeType.GetMethods(BindingFlags.Static | BindingFlags.Public | BindingFlags.NonPublic);
			}
			catch
			{
				methods = null;
			}
			if (methods == null || methods.Length == 0)
			{
				continue;
			}
			for (int i = 0; i < methods.Length; i++)
			{
				MethodInfo methodInfo = methods[i];
				if (methodInfo == null || !HasMonoPInvokeCallbackAttribute(methodInfo))
				{
					continue;
				}
				string text = BuildMethodKey(methodInfo);
				bool flag;
				lock (ExceptionLogLock)
				{
					flag = PatchedMethodKeys.Add(text);
				}
				if (!flag)
				{
					continue;
				}
				try
				{
					_harmony.Patch(methodInfo, finalizer: new HarmonyMethod(typeof(BannerlordExceptionSentinel), "AutoGeneratedCallbackFinalizer"));
					_patchedAutoGeneratedMethodCount++;
				}
				catch (Exception ex)
				{
					LogException("BoundaryPatchInstallFailed", methodInfo, ex, suppress: false, $"assembly={assembly.GetName().Name}", forceDetailed: true);
				}
			}
		}
	}

	private static IEnumerable<Type> GetSafeTypes(Assembly assembly)
	{
		if (assembly == null)
		{
			yield break;
		}
		Type[] array = null;
		try
		{
			array = assembly.GetTypes();
		}
		catch (ReflectionTypeLoadException ex)
		{
			array = ex.Types;
			if (ex.LoaderExceptions != null)
			{
				for (int i = 0; i < ex.LoaderExceptions.Length; i++)
				{
					Exception ex2 = ex.LoaderExceptions[i];
					if (ex2 != null)
					{
						Logger.Log("ExceptionSentinel", $"Type load warning in {assembly.GetName().Name}: {ex2.Message}");
					}
				}
			}
		}
		catch (Exception ex3)
		{
			Logger.Log("ExceptionSentinel", $"Failed to enumerate types from {assembly.GetName().Name}: {ex3.Message}");
			yield break;
		}
		if (array == null)
		{
			yield break;
		}
		for (int j = 0; j < array.Length; j++)
		{
			Type type = array[j];
			if (type != null)
			{
				yield return type;
			}
		}
	}

	private static bool IsManagedCallbackBoundaryType(Type type)
	{
		if (type == null)
		{
			return false;
		}
		string fullName = type.FullName ?? "";
		if (!fullName.StartsWith("ManagedCallbacks.", StringComparison.Ordinal))
		{
			return false;
		}
		return fullName.EndsWith("CallbacksGenerated", StringComparison.Ordinal);
	}

	private static bool HasMonoPInvokeCallbackAttribute(MethodInfo method)
	{
		if (method == null)
		{
			return false;
		}
		try
		{
			return method.CustomAttributes.Any((CustomAttributeData attribute) => string.Equals(attribute.AttributeType.Name, "MonoPInvokeCallbackAttribute", StringComparison.Ordinal));
		}
		catch
		{
			return false;
		}
	}

	private static string BuildMethodKey(MethodBase method)
	{
		if (method == null)
		{
			return "null";
		}
		return $"{method.Module?.Name ?? "unknown"}::{method.DeclaringType?.FullName ?? "unknown"}.{method.Name}";
	}

	public static Exception AutoGeneratedCallbackFinalizer(Exception __exception, MethodBase __originalMethod)
	{
		if (__exception == null)
		{
			return null;
		}
		bool flag = ShouldSuppressBoundaryException(__originalMethod, __exception);
		LogException("BoundaryCallback", __originalMethod, __exception, flag, null, forceDetailed: false);
		return flag ? null : __exception;
	}

	private static bool ShouldSuppressBoundaryException(MethodBase originalMethod, Exception exception)
	{
		if (originalMethod == null || exception == null)
		{
			return false;
		}
		if (exception is OutOfMemoryException || exception is AccessViolationException || exception is BadImageFormatException || exception is CannotUnloadAppDomainException)
		{
			return false;
		}
		MethodInfo methodInfo = originalMethod as MethodInfo;
		if (methodInfo == null || methodInfo.ReturnType != typeof(void))
		{
			return false;
		}
		Type declaringType = originalMethod.DeclaringType;
		string text = declaringType?.FullName ?? "";
		return text.StartsWith("ManagedCallbacks.", StringComparison.Ordinal) && text.EndsWith("CallbacksGenerated", StringComparison.Ordinal);
	}

	private static void LogException(string category, MethodBase method, Exception exception, bool suppress, string extra, bool forceDetailed)
	{
		string text = method?.DeclaringType?.FullName ?? "UnknownType";
		string text2 = method?.Name ?? "UnknownMethod";
		string text3 = exception?.GetType().FullName ?? "UnknownException";
		string text4 = exception?.Message ?? "(no message)";
		string text5 = $"{category}|{text}|{text2}|{text3}|{text4}";
		int num = 1;
		bool flag = forceDetailed;
		lock (ExceptionLogLock)
		{
			if (!ExceptionLogStates.TryGetValue(text5, out var value))
			{
				value = new ExceptionLogState();
				ExceptionLogStates[text5] = value;
			}
			value.Count++;
			num = value.Count;
			DateTime utcNow = DateTime.UtcNow;
			if (forceDetailed || value.Count == 1 || value.Count == 2 || value.Count == 5 || value.Count % 10 == 0 || utcNow - value.LastLoggedUtc > TimeSpan.FromSeconds(30.0))
			{
				flag = true;
				value.LastLoggedUtc = utcNow;
			}
		}
		if (!flag)
		{
			return;
		}
		string text6 = $"{text}.{text2}";
		string text7 = "";
		if (!string.IsNullOrWhiteSpace(extra))
		{
			text7 = " extra=" + extra;
		}
		string text8 = exception?.ToString() ?? "(exception unavailable)";
		Logger.Log("ExceptionSentinel", $"[{category}] count={num} suppress={suppress} method={text6} exception={text3}: {text4}{text7}\n{text8}");
	}
}
