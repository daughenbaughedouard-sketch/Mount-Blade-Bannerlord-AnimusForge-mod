namespace TaleWorlds.CampaignSystem.CampaignBehaviors;

public class TradeAgreementsCampaignBehavior : CampaignBehaviorBase, ITradeAgreementsCampaignBehavior
{
	public class TradeAgreementsCampaignBehaviorTypeDefiner : SaveableTypeDefiner
	{
		public TradeAgreementsCampaignBehaviorTypeDefiner()
			: base(312260)
		{
		}

		protected override void DefineStructTypes()
		{
			AddStructDefinition(typeof(TradeAgreement), 1);
		}

		protected override void DefineContainerDefinitions()
		{
			ConstructContainerDefinition(typeof(List<TradeAgreement>));
		}
	}

	public struct TradeAgreement
	{
		[SaveableField(1)]
		public readonly Kingdom Kingdom1;

		[SaveableField(2)]
		public readonly Kingdom Kingdom2;

		[SaveableField(3)]
		public readonly CampaignTime EndTime;

		[SaveableField(4)]
		public int Kingdom1GoldGained;

		[SaveableField(5)]
		public int Kingdom2GoldGained;

		[SaveableField(6)]
		public int Kingdom1GoldGainedTotal;

		[SaveableField(7)]
		public int Kingdom2GoldGainedTotal;

		public TradeAgreement(Kingdom kingdom1, Kingdom kingdom2, CampaignTime endTime)
		{
			this = default(TradeAgreement);
			Kingdom1 = kingdom1;
			Kingdom2 = kingdom2;
			EndTime = endTime;
			Kingdom1GoldGained = 0;
			Kingdom2GoldGained = 0;
			Kingdom1GoldGainedTotal = 0;
			Kingdom2GoldGainedTotal = 0;
		}

		public TradeAgreement AddGainedGoldToKingdom(Kingdom kingdom, int gold)
		{
			if (kingdom == Kingdom1)
			{
				Kingdom1GoldGained += gold;
				Kingdom1GoldGainedTotal += gold;
			}
			else
			{
				Kingdom2GoldGained += gold;
				Kingdom2GoldGainedTotal += gold;
			}
			return this;
		}

		public TradeAgreement OnGoldSharedInKingdom(Kingdom kingdom, int gold)
		{
			if (kingdom == Kingdom1)
			{
				Kingdom1GoldGained -= gold;
			}
			else
			{
				Kingdom2GoldGained -= gold;
			}
			return this;
		}

		public static void AutoGeneratedStaticCollectObjectsTradeAgreement(object o, List<object> collectedObjects)
		{
			((TradeAgreement)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
		}

		private void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
		{
			collectedObjects.Add(Kingdom1);
			collectedObjects.Add(Kingdom2);
			CampaignTime.AutoGeneratedStaticCollectObjectsCampaignTime(EndTime, collectedObjects);
		}

		internal static object AutoGeneratedGetMemberValueKingdom1(object o)
		{
			return ((TradeAgreement)o).Kingdom1;
		}

		internal static object AutoGeneratedGetMemberValueKingdom2(object o)
		{
			return ((TradeAgreement)o).Kingdom2;
		}

		internal static object AutoGeneratedGetMemberValueEndTime(object o)
		{
			return ((TradeAgreement)o).EndTime;
		}

		internal static object AutoGeneratedGetMemberValueKingdom1GoldGained(object o)
		{
			return ((TradeAgreement)o).Kingdom1GoldGained;
		}

		internal static object AutoGeneratedGetMemberValueKingdom2GoldGained(object o)
		{
			return ((TradeAgreement)o).Kingdom2GoldGained;
		}

		internal static object AutoGeneratedGetMemberValueKingdom1GoldGainedTotal(object o)
		{
			return ((TradeAgreement)o).Kingdom1GoldGainedTotal;
		}

		internal static object AutoGeneratedGetMemberValueKingdom2GoldGainedTotal(object o)
		{
			return ((TradeAgreement)o).Kingdom2GoldGainedTotal;
		}
	}

	private const int BreakingAgreementRelationPenalty = -50;

	private List<TradeAgreement> _tradeAgreements = new List<TradeAgreement>();

	public override void RegisterEvents()
	{
		CampaignEvents.KingdomDestroyedEvent.AddNonSerializedListener(this, OnKingdomDestroyed);
		CampaignEvents.WarDeclared.AddNonSerializedListener(this, WarDeclared);
		CampaignEvents.SettlementEntered.AddNonSerializedListener(this, SettlementEntered);
	}

	private void SettlementEntered(MobileParty party, Settlement settlement, Hero hero)
	{
		if (party != null && party.IsActive && party.MapFaction != null && party.IsCaravan && settlement.IsTown && party.MapFaction != settlement.MapFaction && settlement.MapFaction.IsKingdomFaction && party.MapFaction.IsKingdomFaction && party.IsPartyTradeActive && !party.IsCurrentlyUsedByAQuest && TryGetTradeAgreement((Kingdom)settlement.MapFaction, (Kingdom)party.MapFaction, out var index) && !party.IsFleeing())
		{
			Kingdom kingdom = (Kingdom)settlement.MapFaction;
			_tradeAgreements[index] = _tradeAgreements[index].AddGainedGoldToKingdom(kingdom, Campaign.Current.Models.TradeAgreementModel.GetProfitPerCaravanVisit(party));
		}
	}

	public void OnTradeAgreementOfferedToPlayer(Kingdom fromKingdom)
	{
		if (!Clan.PlayerClan.IsUnderMercenaryService)
		{
			KingdomDecision kingdomDecision = Clan.PlayerClan.Kingdom.UnresolvedDecisions.FirstOrDefault((KingdomDecision s) => s is TradeAgreementDecision tradeAgreementDecision && tradeAgreementDecision.TargetKingdom == fromKingdom);
			if (kingdomDecision != null)
			{
				Clan.PlayerClan.Kingdom.RemoveDecision(kingdomDecision);
			}
			Clan.PlayerClan.Kingdom.AddDecision(new TradeAgreementDecision(TradeAgreementDecision.GetProposerClanForPlayerKingdom(fromKingdom), fromKingdom), ignoreInfluenceCost: true);
		}
		else
		{
			AcceptOffer(fromKingdom);
		}
	}

	private void AcceptOffer(Kingdom fromKingdom)
	{
		MakeTradeAgreement(fromKingdom, Clan.PlayerClan.Kingdom, Campaign.Current.Models.TradeAgreementModel.GetTradeAgreementDurationInYears(fromKingdom, Clan.PlayerClan.Kingdom));
	}

	private void WarDeclared(IFaction faction1, IFaction faction2, DeclareWarAction.DeclareWarDetail detail)
	{
		if (faction1 is Kingdom kingdom && faction2 is Kingdom kingdom2 && HasTradeAgreement(kingdom, kingdom2))
		{
			EndTradeAgreement(kingdom, kingdom2);
			ApplyBrokenTradeAgreementPenalty(kingdom, kingdom2, detail);
		}
	}

	private void ApplyBrokenTradeAgreementPenalty(Kingdom kingdom, Kingdom otherKingdom, DeclareWarAction.DeclareWarDetail detail)
	{
		Hero obj = ((detail == DeclareWarAction.DeclareWarDetail.CausedByPlayerHostility) ? Hero.MainHero : kingdom.Leader);
		ChangeRelationAction.ApplyRelationChangeBetweenHeroes(obj, otherKingdom.Leader, -50);
		if (obj == Hero.MainHero)
		{
			TraitLevelingHelper.OnTradeAgreementBroken();
		}
	}

	private void OnKingdomDestroyed(Kingdom kingdom)
	{
		EndTradeAgreementsOfKingdom(kingdom);
	}

	public override void SyncData(IDataStore dataStore)
	{
		dataStore.SyncData("_tradeAgreements", ref _tradeAgreements);
	}

	public void MakeTradeAgreement(Kingdom kingdom1, Kingdom kingdom2, CampaignTime duration)
	{
		Debug.Print($"Trade agreement signed between {kingdom1.Name} and {kingdom2.Name}");
		TradeAgreement item = new TradeAgreement(kingdom1, kingdom2, CampaignTime.Now + duration);
		_tradeAgreements.Add(item);
		CampaignEventDispatcher.Instance.OnTradeAgreementSigned(kingdom1, kingdom2);
	}

	public void EndTradeAgreementsOfKingdom(Kingdom kingdom)
	{
		_tradeAgreements.RemoveAll((TradeAgreement t) => t.Kingdom1 == kingdom || t.Kingdom2 == kingdom);
	}

	public void EndTradeAgreement(Kingdom kingdom1, Kingdom kingdom2)
	{
		RemoveTradeAgreement(kingdom1, kingdom2);
	}

	private bool HasTradeAgreement(Kingdom kingdom1, Kingdom kingdom2)
	{
		TradeAgreement tradeAgreement;
		return HasTradeAgreement(kingdom1, kingdom2, out tradeAgreement);
	}

	public bool HasTradeAgreement(Kingdom kingdom1, Kingdom kingdom2, out TradeAgreement tradeAgreement)
	{
		bool result = false;
		tradeAgreement = default(TradeAgreement);
		if (TryGetTradeAgreement(kingdom1, kingdom2, out var index))
		{
			tradeAgreement = _tradeAgreements[index];
			if (tradeAgreement.EndTime.IsPast)
			{
				EndTradeAgreement(kingdom1, kingdom2);
			}
			else
			{
				result = true;
			}
		}
		return result;
	}

	public CampaignTime GetTradeAgreementEndDate(Kingdom kingdom1, Kingdom kingdom2)
	{
		if (!TryGetTradeAgreement(kingdom1, kingdom2, out var index))
		{
			Debug.FailedAssert("Cant find trade agreement", "C:\\BuildAgent\\work\\mb3\\Source\\Bannerlord\\TaleWorlds.CampaignSystem\\CampaignBehaviors\\TradeAgreementsCampaignBehavior.cs", "GetTradeAgreementEndDate", 236);
			return CampaignTime.Zero;
		}
		return _tradeAgreements[index].EndTime;
	}

	private bool TryGetTradeAgreement(Kingdom kingdom1, Kingdom kingdom2, out int index)
	{
		index = -1;
		bool result = false;
		for (int i = 0; i < _tradeAgreements.Count; i++)
		{
			TradeAgreement tradeAgreement = _tradeAgreements[i];
			if ((tradeAgreement.Kingdom1 == kingdom1 && tradeAgreement.Kingdom2 == kingdom2) || (tradeAgreement.Kingdom2 == kingdom1 && tradeAgreement.Kingdom1 == kingdom2))
			{
				result = true;
				index = i;
				break;
			}
		}
		return result;
	}

	private void RemoveTradeAgreement(Kingdom kingdom1, Kingdom kingdom2)
	{
		int num = _tradeAgreements.Count - 1;
		while (-1 < num)
		{
			TradeAgreement tradeAgreement = _tradeAgreements[num];
			if ((tradeAgreement.Kingdom1 == kingdom1 && tradeAgreement.Kingdom2 == kingdom2) || (tradeAgreement.Kingdom2 == kingdom1 && tradeAgreement.Kingdom1 == kingdom2))
			{
				_tradeAgreements.RemoveAt(num);
				break;
			}
			num--;
		}
	}

	public void OnTradeGoldDistributedInKingdom(Kingdom kingdom1, Kingdom kingdom2, Clan clan, int share)
	{
		if (TryGetTradeAgreement(kingdom1, kingdom2, out var index))
		{
			_tradeAgreements[index] = _tradeAgreements[index].OnGoldSharedInKingdom(clan.Kingdom, share);
		}
		else
		{
			Debug.FailedAssert("cant find agreement", "C:\\BuildAgent\\work\\mb3\\Source\\Bannerlord\\TaleWorlds.CampaignSystem\\CampaignBehaviors\\TradeAgreementsCampaignBehavior.cs", "OnTradeGoldDistributedInKingdom", 284);
		}
	}
}
