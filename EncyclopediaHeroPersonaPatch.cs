public static class EncyclopediaHeroPersonaPatch
{
	private const string EditButtonId = "AnimusForgeHeroPersonaEditButton";

	private static readonly object SyncRoot = new object();
	private static readonly HashSet<string> GenerationRequests = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
	private static readonly Queue<PendingRefresh> PendingRefreshes = new Queue<PendingRefresh>();
	private static readonly List<WeakReference> LiveGeneratedRoots = new List<WeakReference>();
	private static readonly ConditionalWeakTable<object, EncyclopediaHeroPageVM> RootDataSources = new ConditionalWeakTable<object, EncyclopediaHeroPageVM>();
	private static bool _patched;
	private static bool _loggedFirstButtonState;
	private static bool _loggedButtonCreateProbe;

	private sealed class PendingRefresh
	{
		public WeakReference ViewModel;
		public string HeroId;
	}

	public static void EnsurePatched(Harmony harmony)
	{
		if (_patched)
		{
			return;
		}
		lock (SyncRoot)
		{
			if (_patched)
			{
				return;
			}
			MethodInfo refreshValues = AccessTools.Method(typeof(EncyclopediaHeroPageVM), nameof(EncyclopediaHeroPageVM.RefreshValues));
			if (refreshValues == null)
			{
				Logger.Log("EncyclopediaPersona", "[WARN] EncyclopediaHeroPageVM.RefreshValues not found; skip hero encyclopedia persona text patch.");
				return;
			}
			Harmony activeHarmony = harmony ?? new Harmony("AnimusForge.encyclopedia.hero.persona");
			activeHarmony.Patch(refreshValues, postfix: new HarmonyMethod(typeof(EncyclopediaHeroPersonaPatch), nameof(RefreshValuesPostfix)));

			MethodInfo encyclopediaTick = AccessTools.Method(typeof(EncyclopediaData), "OnTick");
			if (encyclopediaTick != null)
			{
				activeHarmony.Patch(encyclopediaTick, prefix: new HarmonyMethod(typeof(EncyclopediaHeroPersonaPatch), nameof(EncyclopediaDataOnTickPrefix)));
			}
			else
			{
				Logger.Log("EncyclopediaPersona", "[WARN] EncyclopediaData.OnTick not found; text editor input guard will be skipped.");
			}

			MethodInfo loadMovie = AccessTools.Method(typeof(GauntletMovie), nameof(GauntletMovie.Load));
			if (loadMovie != null)
			{
				activeHarmony.Patch(loadMovie, postfix: new HarmonyMethod(typeof(EncyclopediaHeroPersonaPatch), nameof(LoadMoviePostfix)));
			}
			MethodInfo createHeroPage = AccessTools.Method(typeof(GeneratedPrefabCreator), "CreateEncyclopediaHeroPage__TaleWorlds_CampaignSystem_ViewModelCollection_Encyclopedia_Pages_EncyclopediaHeroPageVM");
			if (createHeroPage != null)
			{
				activeHarmony.Patch(createHeroPage, postfix: new HarmonyMethod(typeof(EncyclopediaHeroPersonaPatch), nameof(CreateHeroPagePostfix)));
			}
			MethodInfo setDataSource = AccessTools.Method(typeof(GeneratedHeroPageRoot), "SetDataSource");
			MethodInfo createWidgets = AccessTools.Method(typeof(GeneratedHeroPageRoot), "CreateWidgets");
			if (createWidgets != null)
			{
				activeHarmony.Patch(createWidgets, postfix: new HarmonyMethod(typeof(EncyclopediaHeroPersonaPatch), nameof(CreateWidgetsPostfix)));
			}
			if (setDataSource != null)
			{
				activeHarmony.Patch(setDataSource, postfix: new HarmonyMethod(typeof(EncyclopediaHeroPersonaPatch), nameof(SetDataSourcePostfix)));
				Logger.Log("EncyclopediaPersona", "[INFO] Hero encyclopedia edit button patch enabled.");
			}
			else
			{
				Logger.Log("EncyclopediaPersona", "[WARN] Generated hero encyclopedia page SetDataSource not found; edit button will be skipped.");
			}
			_patched = true;
			Logger.Log("EncyclopediaPersona", "[INFO] Hero encyclopedia persona text patch enabled.");
		}
	}

	public static bool EncyclopediaDataOnTickPrefix()
	{
		return !DevHistoryEditPopup.IsOpen;
	}

	public static void QueueRefreshForHero(string heroId)
	{
		string text = (heroId ?? "").Trim();
		if (string.IsNullOrWhiteSpace(text))
		{
			return;
		}
		lock (SyncRoot)
		{
			PendingRefreshes.Enqueue(new PendingRefresh
			{
				ViewModel = null,
				HeroId = text
			});
		}
	}

	public static void OnApplicationTick()
	{
		ProcessPendingRefreshes();
		UpdateLiveGeneratedRoots();
	}

	public static void RefreshValuesPostfix(EncyclopediaHeroPageVM __instance)
	{
		try
		{
			Hero hero = ResolveHero(__instance);
			ApplyPersonaText(__instance, hero, triggerGeneration: true);
		}
		catch (Exception ex)
		{
			Logger.Log("EncyclopediaPersona", "[WARN] Failed to override hero encyclopedia persona text: " + ex.Message);
		}
	}

	public static void LoadMoviePostfix(string movieName, IViewModel datasource, IGauntletMovie __result)
	{
		try
		{
			if (!string.Equals(movieName, "EncyclopediaHeroPage", StringComparison.Ordinal) || !(datasource is EncyclopediaHeroPageVM vm))
			{
				return;
			}
			Widget root = __result?.RootWidget;
			EnsureEditButton(root);
			TrackGeneratedRoot(root, vm);
			UpdateGeneratedRoot(root, updateText: false);
		}
		catch (Exception ex)
		{
			Logger.Log("EncyclopediaPersona", "[WARN] Failed to inject hero encyclopedia edit button from GauntletMovie.Load: " + ex.Message);
		}
	}

	public static void CreateHeroPagePostfix(GeneratedPrefabInstantiationResult __result)
	{
		try
		{
			Widget root = __result?.Root;
			EnsureEditButton(root);
			TrackGeneratedRoot(root);
			UpdateGeneratedRoot(root, updateText: false);
		}
		catch (Exception ex)
		{
			Logger.Log("EncyclopediaPersona", "[WARN] Failed to inject hero encyclopedia edit button from generated prefab result: " + ex.Message);
		}
	}

	public static void CreateWidgetsPostfix(object __instance)
	{
		try
		{
			EnsureEditButton(__instance);
			TrackGeneratedRoot(__instance);
		}
		catch (Exception ex)
		{
			Logger.Log("EncyclopediaPersona", "[WARN] Failed to create hero encyclopedia edit button: " + ex.Message);
		}
	}

	public static void SetDataSourcePostfix(object __instance)
	{
		try
		{
			EnsureEditButton(__instance);
			TrackGeneratedRoot(__instance);
			UpdateGeneratedRoot(__instance, updateText: false);
		}
		catch (Exception ex)
		{
			Logger.Log("EncyclopediaPersona", "[WARN] Failed to inject hero encyclopedia edit button: " + ex.Message);
		}
	}

	private static void ProcessPendingRefreshes()
	{
		List<PendingRefresh> pending = null;
		lock (SyncRoot)
		{
			if (PendingRefreshes.Count <= 0)
			{
				return;
			}
			pending = new List<PendingRefresh>(PendingRefreshes);
			PendingRefreshes.Clear();
		}
		foreach (PendingRefresh pendingRefresh in pending)
		{
			try
			{
				string heroId = (pendingRefresh?.HeroId ?? "").Trim();
				EncyclopediaHeroPageVM vm = pendingRefresh?.ViewModel?.Target as EncyclopediaHeroPageVM;
				if (vm != null)
				{
					Hero hero = ResolveHero(vm);
					if (string.IsNullOrEmpty(heroId) || string.Equals(hero?.StringId ?? "", heroId, StringComparison.OrdinalIgnoreCase))
					{
						ApplyPersonaText(vm, hero, triggerGeneration: false);
					}
					continue;
				}
				RefreshLiveGeneratedRootsForHero(heroId);
			}
			catch (Exception ex)
			{
				Logger.Log("EncyclopediaPersona", "[WARN] Failed to refresh hero encyclopedia persona text: " + ex.Message);
			}
		}
	}

	private static void UpdateLiveGeneratedRoots()
	{
		List<object> roots = new List<object>();
		lock (SyncRoot)
		{
			for (int i = LiveGeneratedRoots.Count - 1; i >= 0; i--)
			{
				object target = LiveGeneratedRoots[i]?.Target;
				if (target == null)
				{
					LiveGeneratedRoots.RemoveAt(i);
				}
				else
				{
					roots.Add(target);
				}
			}
		}
		foreach (object root in roots)
		{
			UpdateGeneratedRoot(root, updateText: true);
		}
	}

	private static void RefreshLiveGeneratedRootsForHero(string heroId)
	{
		List<object> roots = new List<object>();
		lock (SyncRoot)
		{
			for (int i = LiveGeneratedRoots.Count - 1; i >= 0; i--)
			{
				object target = LiveGeneratedRoots[i]?.Target;
				if (target == null)
				{
					LiveGeneratedRoots.RemoveAt(i);
				}
				else
				{
					roots.Add(target);
				}
			}
		}
		foreach (object root in roots)
		{
			EncyclopediaHeroPageVM vm = GetDataSource(root);
			Hero hero = ResolveHero(vm);
			if (string.IsNullOrWhiteSpace(heroId) || string.Equals(hero?.StringId ?? "", heroId, StringComparison.OrdinalIgnoreCase))
			{
				UpdateGeneratedRoot(root, updateText: true);
			}
		}
	}

	private static void TrackGeneratedRoot(object root, EncyclopediaHeroPageVM vm = null)
	{
		if (root == null)
		{
			return;
		}
		if (vm != null)
		{
			RootDataSources.Remove(root);
			RootDataSources.Add(root, vm);
		}
		lock (SyncRoot)
		{
			foreach (WeakReference weakReference in LiveGeneratedRoots)
			{
				if (ReferenceEquals(weakReference?.Target, root))
				{
					return;
				}
			}
			LiveGeneratedRoots.Add(new WeakReference(root));
		}
	}

	private static void UpdateGeneratedRoot(object root, bool updateText)
	{
		if (root == null)
		{
			return;
		}
		EncyclopediaHeroPageVM vm = GetDataSource(root);
		Hero hero = ResolveHero(vm);
		if (updateText)
		{
			ApplyPersonaText(vm, hero, triggerGeneration: false);
		}
		ButtonWidget button = GetEditButton(root);
		if (button != null)
		{
			bool shouldShow = ShouldShowEditButton(hero, vm);
			button.IsVisible = shouldShow;
			LogFirstButtonState(hero, vm, shouldShow);
		}
	}

	private static void LogFirstButtonState(Hero hero, EncyclopediaHeroPageVM vm, bool visible)
	{
		if (_loggedFirstButtonState || hero == null || vm == null || hero == Hero.MainHero)
		{
			return;
		}
		_loggedFirstButtonState = true;
		Logger.Log("EncyclopediaPersona", "[INFO] Hero encyclopedia edit button state: hero=" + (hero.StringId ?? "") + ", dataManagement=" + MyBehavior.IsDevDataManagementEnabledForExternal() + ", informationHidden=" + vm.IsInformationHidden + ", visible=" + visible + ".");
	}

	private static EncyclopediaHeroPageVM GetDataSource(object root)
	{
		if (root == null)
		{
			return null;
		}
		EncyclopediaHeroPageVM reflected = AccessTools.Field(root.GetType(), "_datasource_Root")?.GetValue(root) as EncyclopediaHeroPageVM;
		if (reflected != null)
		{
			return reflected;
		}
		return RootDataSources.TryGetValue(root, out var cached) ? cached : null;
	}

	private static Hero ResolveHero(EncyclopediaHeroPageVM vm)
	{
		if (vm == null)
		{
			return null;
		}
		return AccessTools.Field(typeof(EncyclopediaHeroPageVM), "_hero")?.GetValue(vm) as Hero;
	}

	private static void ApplyPersonaText(EncyclopediaHeroPageVM vm, Hero hero, bool triggerGeneration)
	{
		if (!ShouldOverride(hero, vm))
		{
			return;
		}
		MyBehavior.GetNpcPersonaForExternal(hero, out var personality, out var background);
		string text = BuildPersonaInformationText(personality, background);
		if (!string.IsNullOrWhiteSpace(text))
		{
			vm.InformationText = text;
			if (triggerGeneration && (string.IsNullOrWhiteSpace(personality) || string.IsNullOrWhiteSpace(background)))
			{
				RequestGeneration(hero, vm);
			}
			return;
		}
		if (!triggerGeneration)
		{
			return;
		}
		vm.InformationText = MyBehavior.BuildNpcPersonaGenerationHintForExternal(hero);
		RequestGeneration(hero, vm);
	}

	private static bool ShouldOverride(Hero hero, EncyclopediaHeroPageVM vm)
	{
		if (hero == null || vm == null || hero == Hero.MainHero)
		{
			return false;
		}
		if (vm.IsInformationHidden)
		{
			return false;
		}
		return hero.CharacterObject?.IsHero == true;
	}

	private static bool ShouldShowEditButton(Hero hero, EncyclopediaHeroPageVM vm)
	{
		return MyBehavior.IsDevDataManagementEnabledForExternal() && ShouldOverride(hero, vm);
	}

	private static string BuildPersonaInformationText(string personality, string background)
	{
		string text = (personality ?? "").Trim();
		string text2 = (background ?? "").Trim();
		if (string.IsNullOrWhiteSpace(text) && string.IsNullOrWhiteSpace(text2))
		{
			return "";
		}
		List<string> parts = new List<string>();
		if (!string.IsNullOrWhiteSpace(text))
		{
			parts.Add("【个性】\n" + text);
		}
		if (!string.IsNullOrWhiteSpace(text2))
		{
			parts.Add("【历史背景】\n" + text2);
		}
		return string.Join("\n\n", parts).Trim();
	}

	private static void RequestGeneration(Hero hero, EncyclopediaHeroPageVM vm)
	{
		if (hero == null || vm == null)
		{
			return;
		}
		string heroId = (hero.StringId ?? "").Trim();
		if (string.IsNullOrWhiteSpace(heroId))
		{
			return;
		}
		if (MyBehavior.TryGetNpcPersonaGenerationStatusForExternal(hero, out var needsGeneration, out var inFlight) && (!needsGeneration || inFlight))
		{
			return;
		}
		lock (SyncRoot)
		{
			if (!GenerationRequests.Add(heroId))
			{
				return;
			}
		}
		_ = GenerateAndQueueRefreshAsync(hero, heroId, vm);
	}

	private static async Task GenerateAndQueueRefreshAsync(Hero hero, string heroId, EncyclopediaHeroPageVM vm)
	{
		try
		{
			await MyBehavior.EnsureNpcPersonaGeneratedForExternalAsync(hero);
			lock (SyncRoot)
			{
				PendingRefreshes.Enqueue(new PendingRefresh
				{
					ViewModel = new WeakReference(vm),
					HeroId = heroId
				});
			}
		}
		catch (Exception ex)
		{
			Logger.Log("EncyclopediaPersona", "[WARN] Failed to trigger hero encyclopedia persona generation: " + ex.Message);
		}
		finally
		{
			lock (SyncRoot)
			{
				GenerationRequests.Remove(heroId);
			}
		}
	}

	private static void EnsureEditButton(object root)
	{
		Widget parent = GetEditButtonParent(root);
		if (parent == null || GetEditButton(root) != null)
		{
			LogButtonCreateProbe(root, parent);
			return;
		}
		ButtonWidget button = new ButtonWidget(parent.Context)
		{
			Id = EditButtonId,
			WidthSizePolicy = SizePolicy.Fixed,
			HeightSizePolicy = SizePolicy.Fixed,
			SuggestedWidth = 180f,
			SuggestedHeight = 38f,
			HorizontalAlignment = HorizontalAlignment.Right,
			VerticalAlignment = VerticalAlignment.Top,
			MarginTop = 8f,
			MarginRight = 44f,
			MarginBottom = 8f,
			Brush = parent.Context.GetBrush("Popup.Done.Button.NineGrid") ?? parent.Context.GetBrush("ButtonBrush2"),
			IsEnabled = true,
			DoNotAcceptEvents = false,
			DoNotPassEventsToChildren = true,
			IsVisible = false
		};
		button.ClickEventHandlers.Add(delegate
		{
			Hero hero = ResolveHero(GetDataSource(root));
			if (hero != null)
			{
				MyBehavior.OpenHeroPersonaEditorForExternal(hero);
			}
		});
		TextWidget textWidget = new TextWidget(parent.Context)
		{
			WidthSizePolicy = SizePolicy.StretchToParent,
			HeightSizePolicy = SizePolicy.StretchToParent,
			Text = "编辑个性与背景",
			Brush = parent.Context.GetBrush("Popup.Button.Text") ?? parent.Context.GetBrush("Encyclopedia.SubPage.Info.Text"),
			DoNotAcceptEvents = true
		};
		if (textWidget.Brush != null)
		{
			textWidget.Brush.FontSize = 18;
			textWidget.Brush.TextHorizontalAlignment = TextHorizontalAlignment.Center;
			textWidget.Brush.TextVerticalAlignment = TextVerticalAlignment.Center;
		}
		button.AddChild(textWidget);
		if (ReferenceEquals(parent, GetRightSideContentList(root)))
		{
			parent.AddChildAtIndex(button, 0);
		}
		else
		{
			parent.AddChild(button);
		}
	}

	private static void LogButtonCreateProbe(object root, Widget parent)
	{
		if (_loggedButtonCreateProbe || root == null)
		{
			return;
		}
		_loggedButtonCreateProbe = true;
		Type rootType = root.GetType();
		Widget rootWidget = root as Widget;
		EncyclopediaHeroPageVM vm = GetDataSource(root);
		Logger.Log("EncyclopediaPersona", "[INFO] Hero encyclopedia edit button probe: rootType=" + rootType.FullName + ", rootWidget=" + (rootWidget != null) + ", parent=" + (parent != null) + ", contentList=" + (GetRightSideContentList(root) != null) + ", rightRoot=" + (GetRightSideRoot(root) != null) + ", dataSource=" + (vm != null) + ", childCount=" + (rootWidget?.ChildCount ?? -1) + ".");
	}

	private static ButtonWidget GetEditButton(object root)
	{
		Widget widget = root as Widget;
		return widget?.FindChild(EditButtonId, includeAllChildren: true) as ButtonWidget;
	}

	private static Widget GetEditButtonParent(object root)
	{
		return GetRightSideContentList(root) ?? GetRightSideRoot(root);
	}

	private static Widget GetRightSideContentList(object root)
	{
		if (root == null)
		{
			return null;
		}
		Widget reflected = AccessTools.Field(root.GetType(), "_widget_0_0_1_0_0_0_0_0")?.GetValue(root) as Widget;
		if (reflected != null)
		{
			return reflected;
		}
		Widget widget = root as Widget;
		Widget clanDivider = widget?.FindChild("ClanDivider", includeAllChildren: true);
		if (clanDivider?.ParentWidget != null)
		{
			return clanDivider.ParentWidget;
		}
		return widget?.FindChild("RightSideList", includeAllChildren: true);
	}

	private static Widget GetRightSideRoot(object root)
	{
		if (root == null)
		{
			return null;
		}
		Widget reflected = AccessTools.Field(root.GetType(), "_widget_0_0_1")?.GetValue(root) as Widget;
		if (reflected != null)
		{
			return reflected;
		}
		Widget widget = root as Widget;
		return widget?.FindChild("RightSideScrollablePanel", includeAllChildren: true);
	}
}
